-- clinics definition

CREATE TABLE clinics (
	CLINIC_ID VARCHAR(50),
	CLINIC_CREATED_AT timestamp,
	BUSINESS_SEGMENTATION VARCHAR(50),
	IS_CHAIN_CLINIC BOOLEAN,
	FIRST_USER_HUBSPOT_SOURCE VARCHAR(50),
	INTEREST_REASONS VARCHAR(128),
	INTEREST_CATEGORY_SIGNUP VARCHAR(50),
	HAS_INTEREST_BNPL BOOLEAN,
	HAS_INTEREST_BNPL_ONLY BOOLEAN,
	HAS_INTEREST_SAAS BOOLEAN,
	TRIAL_DURATION INTEGER,
	TRIAL_START_DATE date,
	TRIAL_END_DATE date,
	TRIAL_START_HOUR INTEGER,
	TRIAL_START_HOUR_CATEGORY VARCHAR(50),
	TRIAL_START_DAY_OF_WEEK INTEGER,
	TRIAL_START_DAY_OF_WEEK_CATEGORY VARCHAR(50),
	HAS_USER_CREATED BOOLEAN,
	MARKETING_ATTRIBUITION VARCHAR(50),
	MARKETING_ATTRIBUITION_CHANNEL_GROUP VARCHAR(50),
	MARKETING_ATTRIBUITION_CAMPAIGN VARCHAR(50),
	MARKETING_ATTRIBUITION_CAMPAIGN_PRODUCT VARCHAR(50),
	MARKETING_ATTRIBUITION_AD_GROUP VARCHAR(50),
	IS_PAID_MEDIUM BOOLEAN,
	HAS_ASKED_FOR_ACCREDITATION BOOLEAN,
	HAS_STARTED_ACCREDITATION_AND_NOT_FINISHED BOOLEAN,
	IS_ACCREDITATION_APPROVED BOOLEAN,
	IS_ACCREDITATION_REPROVED BOOLEAN,
	CLINIC_ACCREDITATION_STATUS VARCHAR(50),
	REQUEST_FINISHED_AT VARCHAR(50),
	ACCREDITATION_REQUESTED_AT timestamp,
	ACCREDITATION_APPROVED_AT timestamp,
	ACCREDITATION_REJECTED_AT timestamp
);